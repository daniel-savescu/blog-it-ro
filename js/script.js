// Pagina "Ce este Linux Shell?"

// 1. ls
function copy_ls_ce_este_linux_shell(){
    let copyCode = document.getElementById("ls_ce_este_linux_shell");
 
    copyCode.select();
    document.execCommand("copy");
             
    document.getElementById("mesaj_pagina_ls_ce_este_linux_shell").innerHTML = "Codul a fost copiat : " + copyCode.value;
}

